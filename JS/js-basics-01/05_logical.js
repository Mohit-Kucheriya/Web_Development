/*
1. && : Logical AND, give boolean result i.e if both condition are true then only true .
 
2. || : Logical OR, give boolean result i.e any one needs to be true.

3. ! : reversed the result. i.e if false -> true and true -> false

*/

// Logical AND
let isLoggedIn = true;
let purchaseCourse = true;

console.log(isLoggedIn && purchaseCourse);

// Logical OR
let emailUser = true;
let googleUser = false;

console.log(emailUser || googleUser);
